x = 20 # disini kita membuat variabel x dengan nilai 20
y = 4 # disini kita membuat variabel y dengan nilai 4
print(x * y)#perkalian
print(x + y)#penjumlahan
print(x - y)#pengurangan
print(x % y)#hasil sisa pembagian nilai variabel
print(x / y)#pembagian dalam bentuk float
print(x // y)#pembagian dalam bentuk bulat