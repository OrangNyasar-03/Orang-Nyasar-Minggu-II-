print("Masukkan Nilai Luas Alas")
la = int(input())
print("Masukkan Nilai Keliling Alas")
ka = int(input())
print("Masukkan Nilai Tinggi")
t = int(input())
luas = 2 * la * ka * t
volume = float(1) / 2 * la * t
print("Jumlah Nilai Dari Luas Prisma Segitiga Adalah: " + str(luas))
print("Jumlah Volume Dari Prisma Segitiga Adalah: " + str(volume))
