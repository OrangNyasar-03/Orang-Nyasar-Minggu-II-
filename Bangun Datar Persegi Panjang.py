print("Menghitung Keliling dan Luas Dari Bangun datar Persegi Panjang")
print("Masukkan Panjang")
panjang = int(input())
print("Masukkan Lebar")
lebar = int(input())
keliling = 2 * (panjang + lebar)
luas = panjang * lebar
print("Keliling Dari Persegi Panjang Adalah: " + str(keliling))
print("Luas Dari Persegi Panjang Adalah: " + str(luas))
